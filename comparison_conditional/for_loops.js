// Topic covers for loops in JavaScript
var classRegister = ["Jeff", "Lawrence", "John"]
for (var i = 0; i < classRegister.length; i++ ){
  console.log(classRegister[i]);
  // Print all elements in array
  // Method increments through array
}

for (var i = classRegister.length - 1; i >= 0; i--){
  console.log(classRegister[i]);
  // De-increments through the array
}
